export async function GET(req: NextRequest) {
  const region   = req.nextUrl.searchParams.get('region') ?? 'US';
  const universe = region === 'KR' ? KR_UNIVERSE : US_UNIVERSE;

  const ranked = [...universe]
    .sort((a, b) => b.cap - a.cap)
    .slice(0, 100)
    .map((item, i) => ({
      rank:      i + 1,
      symbol:    item.symbol,
      name:      item.name,
      price:     0,
      change:    0,
      changePct: 0,
      marketCap: item.cap,
      currency:  region === 'KR' ? 'KRW' : 'USD',
    }));

  return NextResponse.json({ _v: 4, _len: universe.length, items: ranked });
}
